// Enhanced Translation Hook with Context Support
export const useTranslation = (language = 'en') => {
  const t = (key, context = 'common', fallback = null) => {
    try {
      const keys = key.split('.');
      let value = TRANSLATIONS[context]?.[language];
      
      for (const k of keys) {
        value = value?.[k];
      }
      
      // Fallback to English if Arabic translation not found
      if (!value && language === 'ar') {
        value = TRANSLATIONS[context]?.['en'];
        for (const k of keys) {
          value = value?.[k];
        }
      }
      
      return value || fallback || key;
    } catch (error) {
      console.warn(`Translation key '${key}' not found in context '${context}'`);
      return fallback || key;
    }
  };

  // Shortcuts for common contexts
  const tc = (key, fallback = null) => t(key, 'common', fallback);
  const tn = (key, fallback = null) => t(key, 'navigation', fallback);
  const tu = (key, fallback = null) => t(key, 'users', fallback);
  const tp = (key, fallback = null) => t(key, 'products', fallback);
  const tf = (key, fallback = null) => t(key, 'financial', fallback);
  const tcl = (key, fallback = null) => t(key, 'clinical', fallback);
  const ta = (key, fallback = null) => t(key, 'activities', fallback);
  const td = (key, fallback = null) => t(key, 'datetime', fallback);
  const tm = (key, fallback = null) => t(key, 'messages', fallback);

  return { 
    t, 
    tc, 
    tn, 
    tu, 
    tp, 
    tf, 
    tcl, 
    ta, 
    td, 
    tm,
    language,
    isRTL: language === 'ar',
    isEnglish: language === 'en',
    isArabic: language === 'ar'
  };
};

